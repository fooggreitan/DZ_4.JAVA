// и цикл выполняется, пока i меньше чем половина размера списка array. То есть 
// в данном случае цикл будет выполняться два раза, потому что размер списка равен 5, а array.size() / 2 равно 2.
// Внутри цикла на каждой итерации мы меняем местами два элемента списка. Для этого 
