// индексом i. Затем мы меняем значение элемента списка с индексом i на значение элемента 
// списка с индексом array.size() - i - 1, то есть на значение элемента списка, который 
// находится на расстоянии i элементов от конца списка. Наконец, мы меняем значение элемента списка с 
// индексом array.size() - i - 1 на значение переменной temp, которую мы сохранили на предыдущем шаге. 
// Таким образом, мы меняем местами первый элемент списка с последним, второй элемент со вторым с конца, и т.д.
// После завершения цикла весь список array будет перевернут.

